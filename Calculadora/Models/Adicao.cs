class Adicao
{
    static double Somar(double x, double y)
    {
        return (x + y);
    }
        static double Subtrair(double x, double y)
    {
        return (x - y);
    }
    static double Dividir(double x, double y)
    {
        return (x - y);
    }
     static double Mutiplicar(double x, double y)
    {
        return (x * y);
    }
}